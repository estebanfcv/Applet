    private void jbFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFinalizarActionPerformed
        reset = true;
        hilo.suspenderHilo();
        boolean vacio = false;
        for (int i = 0; i < tamanio; i++) {
            if (ListaImagenes.getDescripcion(i).isEmpty()) {
                vacio = true;
                break;
            }
        }
        if (vacio) {
            String titulo = "Notificaci√≥n";
            String mensaje = "¬øDesea finalizar la prueba ? \n Dejo preguntas sin contestar";
            int x = JOptionPane.showConfirmDialog(null, mensaje, titulo, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (x == 0) { // si
                hilo.reanudarHilo();
                hilo.setCorriendo(false);
                insertarDatos();
            } else { // no
                hilo.reanudarHilo();
            }
        } else {
            hilo.reanudarHilo();
            hilo.setCorriendo(false);
            insertarDatos();
        }
    }//GEN-LAST:event_jbFinalizarActionPerformed

